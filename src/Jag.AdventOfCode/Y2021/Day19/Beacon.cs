namespace Jag.AdventOfCode.Y2021.Day19
{
    public class Beacon
    {
        public Beacon(int x, int y, int z)
        {
            X = x;
            Y = y;
            Z = z;
        }

        public int X { get; set; }
        public int Y { get; set; }
        public int Z { get; set; }
    }
}